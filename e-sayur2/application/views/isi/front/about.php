                                                Sejarah Palmeriam dengan Lapak Sayur Mayur Om Hendrik

Menurut sejarah, nama Pal Meriam diambil dari suatu peristiwa yang terjadi pada masa lampau, kira-kira pada 1813.

Pada waktu itu pasukan arteri meriam Inggris mengambil tempat di daerah tersebut untuk posisi meriam yang siap ditembakkan dalam penyerangan ke kota Batavia.

Sehingga, menurut Zaenuddin HM dalam bukunya berjudul 212  Asal-Usul Djakarta Tempo Doeloe, setebal 377 halaman yang diterbitkan Ufuk Press pada Oktober 2012, tempat itu disebut paal meriam (tempat meriam disiapkan). 

Di lokasi, daerah itu dipasang patok jalan yang terbuat dari meriam yang sudah tidak terpakai.

Masyarakat setempat sering melihat meriam sebagai patok jalan atau disebut juga paal jalan yang terbuat dari meriam, maka daerah itu disebut Paal Meriam.

Sedangkan jalan Pal Meriam merupakan tempat di mana kubu ketiga arteri Belanda meledak pada pertempuran di Struijswijk pada 26 Agustus 1811.

