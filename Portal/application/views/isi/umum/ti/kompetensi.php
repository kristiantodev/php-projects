<li align="justify"><b>2. </b>Memiliki kemampuan beradaptasi, bersikap kritis, kreatif, dan inovatif terhadap perkembangan teknologi informasi dan komunikasi (ICT) sehingga dapat lebih berperan dalam era globalisasi.</li>
<li align="justify"><b>3. </b>Memiliki keahlian teknis dalam membangun dan mengembangkan sistem sesuai dengan fungsi dan kegunaannya diberbagai bidang, baik dalam bidang software engineering, database development and administration, network design and administration, computer and network security, web development, dan mobile computing.</li>
<li align="justify"><b>4. </b>Memiliki kemampuan dalam melakukan kegiatan ilmiah, bekerja sama dalam tim, serta berkomunikasi yang baik dan efektif.</li>
