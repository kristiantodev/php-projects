<h4><font color="blue"><b>A. KOMPETENSI UTAMA</b></font></h4>

<p align="justify">Kompetensi utama lulusan Program Studi Teknik Informatika Universitas CIC Cirebon sebagai berikut :</p>
<p>
<ul>
<li align="justify"><b>1. </b>Memiliki dasar matematis dan teori informatika yang kuat untuk diterapkan dalam membangun model solusi dan merancangnya ke dalam sebuah sistem berbasis komputer dan jaringan.</li>
<li align="justify"><b>2. </b>Memiliki kemampuan untuk mengidentifikasi dan menganalisis kebutuhan dalam merancang dan membuat perangkat lunak dan sistem informasi untuk menyelesaikan permasalahan dalam pelaksanaan manajemen dan proses bisnis.</li>
<li align="justify"><b>3. </b>Memahami dan menguasai penggunaan metode, teknik, dan tools dalam melakukan rancang bangun perangkat lunak beserta pendokumentasiannya.</li>
<li align="justify"><b>4. </b>Memiliki kemampuan untuk menerapkan dasar teori dan komponen jaringan beserta faktor keamanannya dalam merancang dan membuat model jaringan sesuai kebutuhan untuk membantu pelaksanaan proses bisnis berbasis jaringan komputer.</li>
<li align="justify"><b>5. </b>Memiliki wawasan pengetahuan yang luas dan menguasai penggunaan perangkat teknologi informasi dan jaringan dalam penerapannya diberbagai bidang aplikasi sesuai dengan perkembangan saat ini dan yang akan datang.</li>
</ul>
