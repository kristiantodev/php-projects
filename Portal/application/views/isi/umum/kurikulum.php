Kurikulum pendidikan yang digunakan pada Program Studi Teknik Informatika STMIK CIC Cirebon adalah Kurikulum PBL.
Apa itu Kurikulum PBL ? Problem Based Learning (PBL) merupakan model pembelajaran yang mendorong mahasiswa untuk lebih aktif
dan memaksimalkan kemampuan berpikir kritis untuk mendapatkan solusi dari masalah pada dunia nyata. Dengan kurikulum PBL,
dapat membuat mahasiswa mahir dalam memecahkan dan mengambil solusi dari suatu masalah, dalam kurikulumnya juga dirancang masalah-masalah
 yang memotivasi untuk mendapatkan pengetahuan yang penting sehingga memiliki strategi belajar sendiri serta kecakapan berpartisipasi dalam kelompok diskusi.
  Proses pembelajarannya menggunakan pendekatan yang sistemik untuk memecahkan masalah atau tantangan yang dibutuhkan dalam kehidupan sehari-hari.

<br><br>Tujuan Kurikulum Problem Based Learning (PBL) adalah membantu mahasiswa mengembangkan keterampilan berpikir dan keterampilan mengatasi masalah,
 belajar peranan orang dewasa yang autentik dan menjadi pembelajar yang mandiri. Sesuai dengan pendapat tersebut, pemecahan masalah merupakan salah satu
  strategi dalam pembelajaran berbasis masalah. PBL sendiri merupakan strategi pembelajaran yang menggunakan masalah dunia nyata sebagai suatu konteks bagi mahasiswa untuk :
  <ul>
    <li>&nbsp;&nbsp;&nbsp;&nbsp;a.&nbsp;&nbsp;Aktif belajar dan menyusun sendiri pengetahuannya.</li>
    <li>&nbsp;&nbsp;&nbsp;&nbsp;b.&nbsp;&nbsp;Memiliki keingintahuan yang tinggi.</li>
    <li>&nbsp;&nbsp;&nbsp;&nbsp;c.&nbsp;&nbsp;Berfikir kritis untuk menyesuaikan dengan pengetahuan baru.</li>
    <li>&nbsp;&nbsp;&nbsp;&nbsp;d.&nbsp;&nbsp;Menganalisa dan berinisiatif terhadap materi yang diberikan.</li>
    <li>&nbsp;&nbsp;&nbsp;&nbsp;e.&nbsp;&nbsp;Mengembangkan keterampilan untuk memecahkan masalah.</li>
    <li>&nbsp;&nbsp;&nbsp;&nbsp;f.&nbsp;&nbsp;Membuat produk yang kreatif.</li>
  </ul>

